public class fourth {
    public static void main(String[] args) {
        // Data Type In Java
        byte b = 125;//range of byte data type is number(-128-127) 1
        byte a = 'a'; // 1 byte
        int c = 2; // 4 byte
        char d = 'a'; // 2 byte
        float e = 1.0f; // 4 byte
        boolean f = true; // 1 byte
        double g = 1.0d; // 8 byte
        long h = 122;   // 8 byte
        short i = 1233; //2 byte
        System.out.println(a);// return ascii value
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
      }
}
