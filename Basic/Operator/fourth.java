
public class fourth {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        // Logical Operator
        System.out.println((a > b) && (b < a));
        System.out.println((a > b) || (b < a));
        System.out.println(!(a > b));
        // assignment operator
        System.out.println(a += 5);
        System.out.println(a -= 5);
        System.out.println(a *= 5);
        System.out.println(a /= 5);
        System.out.println(a %= 5);
    }
}
