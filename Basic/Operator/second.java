public class second {
    public static void main(String[] args) {
        
    
    int a = 10;
    //pre-increment
    int b = ++a;
    System.out.println(a);
    System.out.println(b);

    // post-increment
    int c = 10;
    System.out.println(c);
    int d = c++;
    
    System.out.println(d);

    // pre-decrement
    int x = 10;
    int y = --x;
    System.out.println(x);
    System.out.println(y);

    //post-decrement
    int q = 11;
    int w = q--;
    System.out.println(q);
    System.out.println(w);
    }
}
